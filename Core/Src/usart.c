void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART6) {
		if (Uart1_Rx_Cnt >= 255)  //溢出判断
		{
			Uart1_Rx_Cnt = 0;
			memset(RxBuffer, 0x00, sizeof(RxBuffer));
			HAL_UART_Transmit(&huart6, (uint8_t *) "数据溢出", 10, 0xFFFF);

		} else {
			RxBuffer[Uart1_Rx_Cnt++] = aRxBuffer;   //接收数据转存

			if ((RxBuffer[Uart1_Rx_Cnt - 1] == 0xFF) && (RxBuffer[Uart1_Rx_Cnt - 2] == 0xFF) &&
			    (RxBuffer[Uart1_Rx_Cnt - 3] == 0xFF)) //判断结束位
			{
				if (RxBuffer[0] == 0x55) {
					if (RxBuffer[1] == 0x65) {
						needRunHumidifierTime = RxBuffer[2];
						// 开启时间不为0,打开计时器2
						if (needRunHumidifierTime != 0) {
							// 开启定时器6 并使能中断 加湿器计数定时器
							HAL_TIM_Base_Start_IT(&htim6);
						}
					}
					if (RxBuffer[3] == 0x66) {
						needStopHumidifierTime = RxBuffer[4];
					}
				}
				Uart1_Rx_Cnt = 0;
				memset(RxBuffer, 0x00, sizeof(RxBuffer)); //清空数组
			}
		}
		HAL_UART_Receive_IT(&huart6, (uint8_t *) &aRxBuffer, 1);   //再开启接收中断
	} else if (huart->Instance == USART2) {
		USART_RX_BUF[USART_RX_STA++]=usart2aRxBuffer[0] ;
		if (USART_RX_STA >= 20) {
			USART_RX_STA = 0;
		}
		HAL_UART_Receive_IT(&huart2, (uint8_t *) &usart2aRxBuffer, 1);   //再开启接收中断
	}
}

void UART_clear(){
	USART_RX_STA=0;
	memset(USART_RX_BUF, 0, sizeof (USART_RX_BUF));
}

